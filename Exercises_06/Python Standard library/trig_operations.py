# trig_operations.py - Contains trigonometric functions

from math import sin, cos, radians

def calculate_sine(degrees):
    radians_val = radians(degrees)
    return sin(radians_val)

def calculate_cosine(degrees):
    radians_val = radians(degrees)
    return cos(radians_val)